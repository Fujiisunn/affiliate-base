// ===========================================================================
//  PV(ページビュー)計測の設定 — ここにIDを貼るだけ
// ===========================================================================
//  Google Analytics 4(GA4)を使うと、記事ごとのPV数・訪問者数・流入元などが
//  無料で集計できる。設定はこのファイルだけ編集すればOK(全ページに自動反映)。
//
//  【GA4の始め方】
//   1. https://analytics.google.com/ にアクセスしてアカウントを作成
//   2. 「データストリーム」でウェブサイトを登録(本番URLを入力)
//   3. 表示される「測定ID(G-XXXXXXXXXX の形式)」をコピー
//   4. 下の GA4_MEASUREMENT_ID に貼り付ける
//   5. commit & push してデプロイ → 数分後から計測が始まる
//
//  ※ 空文字 '' のままなら、計測タグは一切出力されない(何も起こらない)。
//  ※ ローカル開発(localhost)では計測を送らない設定にしてある(下の SEND_ON_LOCALHOST)。
// ===========================================================================

// GA4 の測定ID(例: 'G-XXXXXXXXXX')。空ならGA4は無効。
export const GA4_MEASUREMENT_ID = '';

// localhost / 127.0.0.1 でも計測を送るか(通常は false 推奨。テスト時だけ true に)。
export const SEND_ON_LOCALHOST = false;

